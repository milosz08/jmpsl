package pl.miloszgilga.lib.jmpsl.gfx.sender;

import lombok.*;

import pl.miloszgilga.lib.jmpsl.gfx.IBufferedImagePayload;
import pl.miloszgilga.lib.jmpsl.file.hashcode.FileHashCodeGenerator;

/**
 * Simple POJO class storing image sending payload (sending from application). This class implements
 * {@link IBufferedImagePayload} interface and override methods (by lombok annotation). Fields:
 *
 * <ul>
 *     <li><code>bytesRepresentation</code> - image as stream of array bytes</li>
 *     <li><code>preferredWidth</code> - image preferred width (resize by library)</li>
 *     <li><code>preferredHeight</code> - image preferred height (resize by library)</li>
 *     <li><code>id</code> - user id (from database)</li>
 *     <li><code>imageUniquePrefix</code> - user image prefix (avatar, banner etc.)</li>
 *     <li><code>userHashCode</code> - user hash code (generated by {@link FileHashCodeGenerator}, from database)</li>
 * </ul>
 *
 * @author Mi≈Çosz Gilga
 * @since 1.0.2
 */
@Data
@Builder
@AllArgsConstructor
public class BufferedImageSenderPayload implements IBufferedImagePayload {
    private byte[] bytesRepresentation;
    private int preferredWidth;
    private int preferredHeight;
    private Long id;
    private String imageUniquePrefix;
    private String userHashCode;
}
