package pl.miloszgilga.lib.jmpsl.gfx.sender;

import lombok.*;

import pl.miloszgilga.lib.jmpsl.file.hashcode.FileHashCodeGenerator;

/**
 * Simple POJO class representing structure for deleting user image from external SSH/SFTP static resources server.
 * Class has following parameters:
 *
 * <ul>
 *     <li><code>userHashCode</code> - user hash code (generated by {@link FileHashCodeGenerator}, from database)</li>
 *     <li><code>uniqueImagePrefix</code> - user image prefix (avatar, banner etc.)</li>
 *     <li><code>id</code> - user id (from database)</li>
 * </ul>
 *
 * @author Mi≈Çosz Gilga
 * @since 1.0.2
 */
@Data
@Builder
@AllArgsConstructor
public class BufferedImageDeletePayload {
    private String userHashCode;
    private String uniqueImagePrefix;
    private Long id;
}
